def print_gugudan(dan):
    """
    특정 단수를 입력받아 구구단을 출력하는 함수입니다.
    """
    print(f"\n--- {dan}단 시작! ---")
    
    # 1부터 9까지 반복합니다. (range는 마지막 숫자를 포함하지 않으므로 10까지 적습니다)
    for i in range(1, 10):
        result = dan * i
        print(f"{dan} x {i} = {result}")
        
    print("------------------\n")

# 사용자가 0을 입력할 때까지 계속해서 구구단을 물어보는 프로그램
while True:
    # input()으로 받은 값은 문자열(글자)이므로 int()를 써서 숫자로 바꿔줍니다.
    user_input = int(input("출력하고 싶은 구구단의 단수를 입력하세요 (종료하려면 0 입력): "))
    
    # 0이 입력되면 while 반복문을 탈출(break)합니다.
    if user_input == 0:
        print("구구단 프로그램을 종료합니다. 수고하셨습니다!")
        break
        
    # 0이 아니라면 위에서 만든 함수를 실행합니다.
    print_gugudan(user_input)
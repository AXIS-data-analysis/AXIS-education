"""
문제 3: 더치페이와 남은 돈 (몫과 나머지 연산자)
친구들과 맛있는 저녁을 먹고 총 134,000원이 나왔습니다. 함께 먹은 친구들은 본인 포함 총 6명입니다. 이 금액을 6명이서 1원 단위까지 똑같이 나누어 내려고 합니다.

총금액과 사람 수를 각각 변수에 저장해 주세요.

한 사람이 내야 할 금액(몫)은 얼마인지 연산자를 사용해 구하고 출력해 보세요.

똑같이 나누고 남은 자투리 금액(나머지)은 얼마인지 연산자를 사용해 구하고 출력해 보세요.
(힌트: 나누기 기호 대신 // 와 % 연산자를 적극적으로 활용해 보세요!)
"""
### 모범답안 ###
total_money = 134000
people = 6

# 몫 구하기
pay_per_person = total_money // people
print(pay_per_person)

# 나머지 구하기
leftover = total_money % people
print(leftover)
for lap in range(1, 6):
    # 2. f-string을 활용하여 아래와 같이 출력하세요.
    #    * 출력 예시: 1바퀴째 뛰고 있습니다!
    print(f"{lap}바퀴째 뛰고 있습니다!")

"""
문제 2. 딕셔너리와 for문 활용
다음은 어느 카페의 메뉴와 가격이 담긴 딕셔너리입니다.
for문을 사용하여 메뉴의 이름(key)과 가격(value)을 모두 출력하는 코드를 작성하세요.

menu_prices = {"아메리카노": 4500, "카페라떼": 5000, "딸기스무디": 6000}
"""
### 모범답안 ###
menu_prices = {"아메리카노": 4500, "카페라떼": 5000, "딸기스무디": 6000}

# 1. for문과 items() 메소드를 사용하여 딕셔너리의 key와 value를 동시에 가져오세요.
for menu, price in menu_prices.items():
    # 2. f-string을 사용하여 아래와 같이 출력하세요.
    #    * 출력 예시: 아메리카노의 가격은 4500원입니다.
    print(f"{menu}의 가격은 {price}원입니다.")

"""
문제 3. while, break, continue의 복합 활용
엘리베이터가 1층부터 10층까지 올라가는 과정을 시뮬레이션 하려고 합니다.
- 엘리베이터는 1층씩 올라갑니다.
- 4층은 공사 중이므로 멈추지 않고 건너뜁니다. (continue 활용)
- 8층에 도착하면 내릴 사람이 있어서 운행을 종료합니다. (break 활용)
주어진 변수를 활용하여 조건에 맞게 동작하는 while문 코드를 작성하세요.

floor = 1
"""
### 모범답안 ###
floor = 1

# 1. while문을 사용하여 floor가 10 이하일 때까지 반복되도록 설정하세요.
while floor <= 10:
    # 2. 만약 floor가 4라면, 층수를 1 증가시키고 이번 반복을 건너뛰세요. (continue)
    if floor == 4:
        floor += 1
        continue
        
    # 3. 현재 층수를 출력하세요. (예: "현재 1층입니다.")
    print(f"현재 {floor}층입니다.")
    
    # 4. 만약 floor가 8이라면, 반복문을 즉시 탈출하세요. (break)
    if floor == 8:
        print("8층에서 운행을 종료합니다.")
        break
        
    # 5. 다음 반복을 위해 층수를 1 증가시킵니다.
    floor += 1
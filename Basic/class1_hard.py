"""
문제 1. 동적 카페 포스기 만들기 (input, 형 변환, 연산)
기존에는 정해진 아메리카노 가격만 계산했지만,
이제는 어떤 메뉴든 계산할 수 있는 포스기를 만들어봅시다.
"""
### 모범답안 ###
# 1. "input()" 함수를 사용하여 '메뉴 이름', '1잔 가격', '주문 수량'을 차례대로 사용자에게 입력받아 각각 변수에 저장해 보세요.
menu_name = input("메뉴 이름을 입력하세요: ")
price = int(input("1잔 가격을 입력하세요: "))
count = int(input("주문 수량을 입력하세요: "))

# 2. 입력받은 가격과 주문 수량을 활용해 총 결제 금액을 계산해 새로운 변수에 저장해 보세요.
total_price = price * count

# 3. 계산된 총 금액을 메뉴 이름과 함께 결과로 출력해 보세요.
#    (출력 예시: "입력하신 바닐라라떼 3잔의 총 결제 금액은 13500원입니다.")
print(f"입력하신 {menu_name} {count}잔의 총 결제 금액은 {total_price}원입니다.")

"""
문제 2. 나만의 프로필 생성기 (input과 문자열 포매팅)
입력받은 데이터를 바탕으로 자연스러운 자기소개 문구를 자동으로 만들어주는 프로그램을 작성해 봅시다.
"""
### 모범답안 ###
# 1. "input()"을 사용해 사용자의 '이름'과 '태어난 연도'를 입력받으세요.
name = input("이름을 입력하세요: ")
birth_year = int(input("태어난 연도를 입력하세요: "))

# 2. 태어난 연도를 활용하여 올해의 나이를 계산하는 수식을 작성하고 변수에 저장해 보세요.
current_year = 2026
age = current_year - birth_year

# 3. 문자열 포매팅(f-string 등)을 활용하여 숫자와 문자를 자연스럽게 결합해 아래와 같은 형태의 문장으로 출력해 보세요.
#    (출력 예시: "안녕하세요! 제 이름은 홍길동이고, 올해 나이는 25세입니다.")
print(f"안녕하세요! 제 이름은 {name}이고, 올해 나이는 {age}세입니다.")

"""
문제 3. 영수증 정산 마스터 (input, 몫과 나머지 연산, 서식 지정)
고정된 금액이 아니라, 매번 달라지는 영수증 금액과 인원수에 대응해 더치페이 금액을 계산해 봅시다.
"""
### 모범답안 ###
# 1. 사용자에게 '총 회식비 금액'과 '참석한 사람 수'를 "input()"으로 입력받아 변수에 저장하세요.
total_amount = int(input("총 회식비 금액을 입력하세요: "))
people_count = int(input("참석한 사람 수를 입력하세요: "))

# 2. 1인당 내야 할 금액(몫)과 똑같이 나누고 남은 자투리 금액(나머지)을 연산자를 사용해 각각 계산하세요.
per_person = total_amount // people_count
remainder = total_amount % people_count

# 3. 계산된 결과를 바탕으로 아래 예시처럼 한 문장으로 정리해서 출력해 보세요.
#    (출력 예시: "총 금액 134000원을 6명에서 나누면 1인당 22333원씩 내야 하고, 2원이 남습니다.")
print(f"총 금액 {total_amount}원을 {people_count}명에서 나누면 1인당 {per_person}원씩 내야 하고, {remainder}원이 남습니다.")
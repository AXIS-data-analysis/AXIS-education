"""
문제 3. 지역 변수(Local)와 전역 변수(Global)의 이해
게임 캐릭터의 레벨을 관리하는 프로그램입니다.
함수 안에서 바깥쪽(전역)에 있는 레벨 변수의 값을 직접 수정할 수 있도록 알맞은 키워드를 사용하세요.

level = 10
"""
### 모범답안 ###
level = 10

def level_up():
    # 1. 함수 외부의 전역 변수 'level'을 함수 내부에서 수정하기 위한 키워드를 작성하세요.
    global level
    
    # 레벨을 1 증가시킵니다.
    level += 1
    print(f"레벨업! 현재 레벨: {level}")

# 2. level_up() 함수를 2번 호출하세요.
level_up()
level_up()

# 3. 마지막으로 바깥에서 level 변수를 출력하여 값이 정상적으로 올랐는지 확인하세요.
print(f"최종 캐릭터 레벨: {level}")
unique_sales = list(unique_dict)

# 3. f-string을 사용하여 중복이 제거된 판매 음료 목록을 출력하세요.
#    * 출력 결과: 순서가 유지된 중복 제거 목록: ['아메리카노', '라떼', '밀크티', '에이드']
print(f"순서가 유지된 중복 제거 목록: {unique_sales}")


"""
문제 2. 자료형 선택과 변환
다음은 절대로 값이 변경되거나 삭제되면 안 되는 중요한 메인 서버의 IP 주소들입니다.
현재 리스트로 저장되어 있는 데이터를, 데이터가 수정될 수 없도록 알맞은 자료형으로 변환하고, 
데이터가 정상적으로 들어있는지 인덱싱을 통해 확인하는 코드를 작성하세요.

ip_addresses = ["192.168.0.1", "10.0.0.1", "172.16.0.1"]
"""
### 모범답안 ###
ip_addresses = ["192.168.0.1", "10.0.0.1", "172.16.0.1"]

# 1. list 자료형을 데이터 수정이 불가능한 tuple 자료형으로 변환하여 변수에 저장하세요.
ip_tuple = tuple(ip_addresses)

# 2. f-string과 인덱싱을 사용하여 첫 번째 IP 주소를 출력하세요.
#    * 출력 결과: 메인 서버 IP: 192.168.0.1
print(f"메인 서버 IP: {ip_tuple[0]}")


"""
문제 3. 조건문 (if, elif, else) 활용
놀이공원의 탑승 기구는 키(height)에 따라 탑승 가능 여부가 다릅니다.
주어진 키 데이터를 바탕으로 조건에 맞게 출력하는 코드를 작성하세요.
- 키가 150 이상이면 "모든 기구 탑승 가능"
- 키가 130 이상, 150 미만이면 "일부 기구 탑승 가능"
- 키가 130 미만이면 "보호자 동반 필요"

height = 145
"""
### 모범답안 ###
height = 145

# 1. if, elif, else 조건문을 사용하여 키에 따른 탑승 가능 여부를 판별하는 코드를 작성하세요.
if height >= 150:
    result = "모든 기구 탑승 가능"
elif height >= 130:
    result = "일부 기구 탑승 가능"
else:
    result = "보호자 동반 필요"

# 2. f-string을 활용하여 결과를 출력하세요.
#    * 출력 결과: 손님의 키는 145cm이며, 일부 기구 탑승 가능합니다.
print(f"손님의 키는 {height}cm이며, {result}합니다.")
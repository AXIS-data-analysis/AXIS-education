"""
문제 1. 자료형 변환과 중복 제거 (순서 유지)
다음은 한 달 동안 카페에서 판매된 음료의 기록(리스트)입니다.
자료형 변환을 사용하여 중복된 음료 이름을 제거하되, 판매된 순서를 유지하여 
새로운 리스트로 만들고 출력하는 코드를 작성하세요.

sales_log = ["아메리카노", "라떼", "아메리카노", "밀크티", "라떼", "에이드"]
"""
### 모범답안 ###
sales_log = ["아메리카노", "라떼", "아메리카노", "밀크티", "라떼", "에이드"]

# 1. dict.fromkeys()를 사용하여 리스트의 순서를 유지하며 중복을 제거한 딕셔너리를 만드세요.
unique_dict = dict.fromkeys(sales_log)


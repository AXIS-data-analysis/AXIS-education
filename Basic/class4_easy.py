"""
문제 1. 튜플(Tuple) 언패킹과 별표(*) 활용
다음은 학생들의 시험 점수가 담긴 튜플입니다.
첫 번째와 두 번째 점수를 각각 변수에 담고, 나머지 점수들은 
별표(*)를 활용하여 리스트 형태로 묶어서 언패킹하는 코드를 작성하세요.

scores = (95, 85, 90, 75, 80, 100)
"""
### 모범답안 ###
scores = (95, 85, 90, 75, 80, 100)

# 1. 첫 번째 점수, 두 번째 점수, 그리고 나머지 점수들(* 활용)을 언패킹하여 변수에 할당하세요.
(first, second, *others) = scores

# 2. f-string을 사용하여 언패킹된 변수들을 아래와 같이 출력하세요.
#    * 출력 결과: 1등 점수: 95, 2등 점수: 85, 나머지 점수: [90, 75, 80, 100]
print(f"1등 점수: {first}, 2등 점수: {second}, 나머지 점수: {others}")

"""
문제 2. 세트(Set)의 집합 연산과 데이터 다루기
철수와 영희가 좋아하는 과일 세트가 주어졌습니다.
교집합과 합집합을 구하는 메소드를 활용하고, 값을 추가/삭제하는 코드를 작성하세요.

chulsoo_fruits = {"사과", "바나나", "포도"}
younghee_fruits = {"바나나", "딸기", "오렌지", "포도"}
"""
### 모범답안 ###
chulsoo_fruits = {"사과", "바나나", "포도"}
younghee_fruits = {"바나나", "딸기", "오렌지", "포도"}

# 1. intersection() 메소드를 사용하여 두 사람이 공통으로 좋아하는 과일(교집합)을 변수에 저장하고 출력하세요.
common_fruits = chulsoo_fruits.intersection(younghee_fruits)
print(f"공통으로 좋아하는 과일: {common_fruits}")

# 2. add() 메소드를 사용하여 철수의 과일 세트에 "수박"을 추가하세요.
chulsoo_fruits.add("수박")

# 3. remove() 메소드를 사용하여 영희의 과일 세트에서 "오렌지"를 삭제하세요.
younghee_fruits.remove("오렌지")

# 4. union() 메소드를 사용하여 수정된 두 사람의 모든 과일 종류(합집합)를 구하고 출력하세요.
all_fruits = chulsoo_fruits.union(younghee_fruits)
print(f"모든 과일 종류: {all_fruits}")

"""
문제 3. 딕셔너리(Dictionary) 값 추가 및 수정
다음 학생의 정보가 담긴 딕셔너리를 바탕으로,
새로운 정보를 추가하고 여러 기존 정보를 한 번에 수정한 뒤 확인하는 코드를 작성하세요.

student = {"이름": "김파이", "나이": 20, "전공": "컴퓨터공학"}
"""
### 모범답안 ###
student = {"이름": "김파이", "나이": 20, "전공": "컴퓨터공학"}

# 1. 딕셔너리에 "학점" 이라는 key와 4.0 이라는 value를 새롭게 추가하세요.
student["학점"] = 4.0

# 2. update() 메소드를 사용하여 "나이"를 21로, "전공"을 "데이터사이언스"로 한 번에 수정하세요.
student.update({"나이": 21, "전공": "데이터사이언스"})

# 3. keys() 메소드를 사용하여 student 딕셔너리의 모든 key들만 출력하세요.
print(student.keys())

# 4. values() 메소드를 사용하여 student 딕셔너리의 모든 value들만 출력하세요.
print(student.values())
import pandas as pd
import numpy as np

# =========================================================
# [사전 준비] 실습용 데이터셋(CSV) 자동 생성
# =========================================================
print("데이터셋 생성을 시작합니다...")

# 재현성을 위해 난수 시드 고정
np.random.seed(42)
n_rows = 150

# 가상의 쇼핑몰 데이터 생성
df_gen = pd.DataFrame({
    "주문번호": [f"ORD{str(i).zfill(4)}" for i in range(1, n_rows + 1)],
    "고객ID": [f"USER{str(np.random.randint(1, 50)).zfill(3)}" for _ in range(n_rows)],
    "카테고리": np.random.choice(["전자기기", "의류", "식품", "생활용품", "도서"], n_rows, p=[0.25, 0.3, 0.2, 0.15, 0.1]),
    "단가": np.random.randint(1, 100, n_rows) * 10000,
    "수량": np.random.randint(1, 6, n_rows)  # 1개 ~ 5개
})

# 한글 깨짐 방지를 위해 cp949 인코딩으로 저장
df_gen.to_csv("shopping_sales.csv", index=False, encoding="cp949")
print("✅ 실습용 데이터셋('shopping_sales.csv') 생성 완료!\n")


# ==========================================
# [AXIS Core반] 2회차 문제 (심화) - 모범답안
# ==========================================

"""
"문제 1. 새로운 파생 변수(컬럼) 생성 및 수정"
쇼핑몰 매출 데이터('shopping_sales.csv')를 불러와 본격적인 분석을 준비합니다.
데이터프레임에 있는 '단가'와 '수량' 컬럼을 곱하여 '총결제금액' 컬럼을 새로 생성하고,
모든 '총결제금액'에 VIP 특별 할인율(10%)을 일괄 적용한 '할인적용금액' 컬럼을 추가하는 코드를 작성하세요.
"""
### 모범답안 ###
# 위에서 생성한 파일을 불러옵니다. (한글 인코딩 cp949 지정)
df = pd.read_csv("shopping_sales.csv", encoding="cp949")

# 1. Series 연산을 활용하여 '단가'와 '수량' 컬럼을 곱한 뒤, '총결제금액' 파생 변수를 생성하세요.
df["총결제금액"] = df["단가"] * df["수량"]

# 2. '총결제금액'에 0.9를 곱하여 10% 할인이 적용된 '할인적용금액' 컬럼을 새로 추가하세요.
df["할인적용금액"] = df["총결제금액"] * 0.9

# 3. 상위 3개의 행을 출력하여 새로운 컬럼들이 의도한 대로 잘 추가되었는지 확인하세요.
print("--- [문제 1] 파생 변수 추가 결과 ---")
print(df.head(3), "\n")


"""
"문제 2. 다중 조건 필터링과 특정 열 선택 (loc 인덱서)"
특정 타겟 고객층의 소비 패턴을 분석하기 위해 세밀하게 데이터를 추출해야 합니다.
카테고리가 '전자기기'이면서 '수량'이 3개 이상인 대량 구매 건을 찾고, 
해당 구매 건의 '주문번호', '고객ID', '할인적용금액' 컬럼만 추출하여 새로운 데이터프레임으로 저장하세요.
"""
### 모범답안 ###
# 1. '카테고리'가 '전자기기'이고, '수량'이 3 이상인 다중 조건을 작성하세요.
target_cond = (df["카테고리"] == "전자기기") & (df["수량"] >= 3)

# 2. loc 인덱서를 사용하여 조건에 맞는 행(row)과, 분석에 필요한 3개의 열(column)만 추출해 'target_df' 변수에 저장하세요.
target_df = df.loc[target_cond, ["주문번호", "고객ID", "할인적용금액"]]

# 3. 추출된 데이터의 크기(shape)를 확인하여 필터링이 잘 되었는지 점검하세요.
print("--- [문제 2] 다중 조건 필터링 결과 ---")
print("조건 만족 데이터 크기:", target_df.shape, "\n")


"""
"문제 3. 데이터 정렬 및 인덱스 초기화 (sort_values, reset_index)"
추출한 타겟 데이터를 결제 금액이 높은 VIP 구매 건 순서대로 정리하고, 인덱스를 새롭게 부여해 봅시다.
target_df를 '할인적용금액' 기준으로 내림차순 정렬한 뒤, 
중구난방이 된 기존 인덱스를 버리고 새로운 0부터 시작하는 인덱스로 초기화하는 코드를 작성하세요.
"""
### 모범답안 ###
# 1. sort_values()를 사용하여 '할인적용금액' 기준으로 데이터를 내림차순 정렬하세요.
sorted_df = target_df.sort_values(by="할인적용금액", ascending=False)

# 2. reset_index()를 사용하여 인덱스를 0부터 순서대로 초기화하세요. 
# (이때 기존 인덱스 번호가 새로운 데이터 열로 남지 않도록 drop=True 속성을 사용하세요.)
final_df = sorted_df.reset_index(drop=True)

# 3. 정렬 및 인덱스 초기화가 완료된 최종 데이터의 상위 5건을 출력하세요.
print("--- [문제 3] 정렬 및 인덱스 초기화 결과 ---")
print(final_df.head(5))
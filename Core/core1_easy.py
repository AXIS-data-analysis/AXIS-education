"""
"문제 1. 실전 공공데이터 불러오기 (인코딩 및 인덱스 설정)"
실제 한국 공공데이터(CSV)는 한글 인코딩 문제로 인해 기본 설정으로 불러오면 글씨가 깨지는 경우가 많습니다.
'seoul_bike_data.csv' (서울시 따릉이 대여소 정보) 파일을 한글이 깨지지 않도록 불러오고, 
'대여소번호' 컬럼을 데이터프레임의 인덱스로 지정하는 코드를 작성하세요.
"""
### 모범답안 ###

# 1. read_csv()를 사용하되, 한글 깨짐 방지를 위해 encoding="cp949" 파라미터를 추가하세요.
# 2. index_col 파라미터를 사용하여 '대여소번호' 컬럼을 인덱스로 설정하여 'df' 변수에 저장하세요.
df = pd.read_csv("seoul_bike_data.csv", encoding="cp949", index_col="대여소번호")

# 3. 데이터프레임의 상위 3개 행을 출력하여 인덱스가 올바르게 설정되었는지 확인하세요.
print("--- [문제 1] 인코딩 및 인덱스 설정 결과 ---")
print(df.head(3), "\n")


"""
"문제 2. 데이터프레임 조건부 탐색 (loc 인덱서 활용)"
데이터의 전반적인 구조를 파악했다면, 이제 분석 목적에 맞는 특정 데이터만 뽑아볼 차례입니다.
따릉이 거치대수가 20개 이상인 대규모 대여소의 이름과 위치 정보만 추출하는 코드를 작성하세요.
"""
### 모범답안 ###
# 1. df에서 '거치대수'가 20 이상인 행(row)만 찾는 조건식(불리언 인덱싱)을 만드세요.
condition = df["거치대수"] >= 20

# 2. loc 인덱서를 활용하여, 위 조건을 만족하는 행들의 '대여소명'과 '소재지(구)' 컬럼만 추출해 'large_stations' 변수에 저장하세요.
large_stations = df.loc[condition, ["대여소명", "소재지(구)"]]

# 3. 추출된 large_stations의 행, 열 크기(shape)를 출력하여 몇 개의 대여소가 조건에 맞는지 확인하세요.
print("--- [문제 2] 조건부 탐색 결과 ---")
print("대규모 대여소 데이터 크기:", large_stations.shape, "\n")


"""
"문제 3. 다중 조건 필터링 및 데이터 정렬 (sort_values)"
현업 데이터 분석에 맞춰 2개 이상의 조건을 결합해보고, 데이터를 보기 좋게 정렬해봅시다.
'소재지(구)'가 '강남구'이면서 '거치대수'가 15개 이상인 대여소들을 찾고,
거치대가 많은 순서대로(내림차순) 정렬하는 코드를 작성하세요.
"""
### 모범답안 ###
# 1. '소재지(구)'가 '강남구'인 조건과 '거치대수'가 15 이상인 조건을 모두 만족(& 연산자)하는 필터를 만드세요.
gangnam_cond = (df["소재지(구)"] == "강남구") & (df["거치대수"] >= 15)

# 2. 위 조건을 만족하는 데이터를 추출한 뒤, sort_values()를 사용해 '거치대수'를 기준으로 내림차순(ascending=False) 정렬하여 'gangnam_top'에 저장하세요.
gangnam_top = df[gangnam_cond].sort_values(by="거치대수", ascending=False)

# 3. 결과 데이터프레임의 상위 5개를 출력하여 가장 거치대가 많은 강남구 대여소들을 확인하세요.
print("--- [문제 3] 강남구 대여소 정렬 결과 ---")
print(gangnam_top.head(5))
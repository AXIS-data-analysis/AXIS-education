import pandas as pd
import numpy as np

# =========================================================
# [사전 준비] 실습용 데이터셋(CSV) 자동 생성
# =========================================================
print("데이터셋 생성을 시작합니다...")

# 재현성을 위해 난수 시드 고정
np.random.seed(42)
n_stations = 150
gu_list = ["강남구", "서초구", "송파구", "마포구", "종로구", "영등포구", "관악구", "동작구"]

# 서울시 따릉이 가상 데이터 생성
df_bike_gen = pd.DataFrame({
    "대여소번호": [f"ST-{1000 + i}" for i in range(n_stations)],
    "대여소명": [f"따릉이대여소_{i}번" for i in range(n_stations)],
    "소재지(구)": np.random.choice(gu_list, n_stations),
    "거치대수": np.random.randint(5, 35, n_stations) # 5개 ~ 34개
})

# 학생들이 cp949 디코딩을 실습할 수 있도록, 저장할 때 cp949로 인코딩하여 저장
df_bike_gen.to_csv("seoul_bike_data.csv", index=False, encoding="cp949")
print("✅ 실습용 데이터셋('seoul_bike_data.csv') 생성 완료!\n")


